public class JavaMathExample {
    public static void main(String[] args) {
        
        double x =26;
        double y= 43;

        System.out.println("max number of x and y is:"+Math.max(x,y));
        System.out.println("Square root of y is "+Math.sqrt(y));
        System.out.println("Power of x and y is "+Math.pow(x, y));
        System.out.println("Logatithm of x "+Math.log(x));
        System.out.println("Logatithm of y "+Math.log(y));
        System.out.println("Log10 of x is "+Math.log10(x));
        System.out.println("Log10 of y is "+Math.log10(y));
        System.out.println("log1p of x is "+Math.log1p(x));
        System.out.println("log1p of y is "+Math.log1p(y));
        System.out.println("exp of x is "+Math.exp(x));
        System.out.println("expm1 of y is "+Math.expm1(x));

    }
}
